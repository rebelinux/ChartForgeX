namespace ChartForgeX.Core;

/// <summary>
/// Defines built-in shapes used by pictorial charts.
/// </summary>
public enum ChartPictorialShape {
    /// <summary>
    /// Renders circular symbols.
    /// </summary>
    Circle,

    /// <summary>
    /// Renders rounded square symbols.
    /// </summary>
    Square,

    /// <summary>
    /// Renders diamond symbols.
    /// </summary>
    Diamond,

    /// <summary>
    /// Renders upward triangle symbols.
    /// </summary>
    Triangle,

    /// <summary>
    /// Renders five-point star symbols.
    /// </summary>
    Star,

    /// <summary>
    /// Renders heart symbols.
    /// </summary>
    Heart,

    /// <summary>
    /// Renders shield symbols.
    /// </summary>
    Shield,

    /// <summary>
    /// Renders check mark symbols.
    /// </summary>
    Check,

    /// <summary>
    /// Renders person symbols.
    /// </summary>
    Person,

    /// <summary>
    /// Renders person symbols with a dress silhouette.
    /// </summary>
    PersonDress
}
